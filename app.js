resetBtn.addEventListener('click', () => {
  comunaFilter.value = 'TODAS';
  searchInput.value = '';
  currentData = [...electoralData];
  renderAll();
});

fitBtn.addEventListener('click', () => {
  renderMarkers(currentData);
});

