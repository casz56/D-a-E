function exportarExcel() {
  if (typeof XLSX === 'undefined') {
    alert('No fue posible cargar la librería de Excel.');
    return;
  }
  const rows = currentData.map(item => ({
    'Puesto': item.puesto,
    'Comuna / corregimiento': item.comuna,
    'Dirección': item.direccion,
    'Mesas': item.mesas,
    'Mujeres': item.mujeres,
    'Hombres': item.hombres,
    'Total': item.total,
    'Latitud': item.lat,
    'Longitud': item.lng,
  }));

  const worksheet = XLSX.utils.json_to_sheet(rows);
  const workbook = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(workbook, worksheet, 'Puestos');

  const range = XLSX.utils.decode_range(worksheet['!ref']);
  const widths = []
  for (let C = range.s.c; C <= range.e.c; ++C) {
    let maxLen = 10;
    for (let R = range.s.r; R <= range.e.r; ++R) {
      const cell = worksheet[XLSX.utils.encode_cell({ r: R, c: C })];
      if (cell && cell.v != null) maxLen = Math.max(maxLen, String(cell.v).length);
    }
    widths.push({ wch: Math.min(maxLen + 2, 40) });
  }
  worksheet['!cols'] = widths;

  XLSX.writeFile(workbook, 'detalle_puestos_votacion_neiva.xlsx');
}


window.addEventListener('DOMContentLoaded', () => {
  const excelBtn = document.getElementById('excelBtn');
  if (excelBtn) excelBtn.addEventListener('click', exportarExcel);
});
